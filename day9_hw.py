import turtle as t
t.bgcolor("purple")#배경색을 보라색으로 설정합니다
for x in range(20):#0부터 19까지 20번 반복합니다
    t.color("white") #펜 색을 하얀색으로 설정합니다.
    t.forward(50)#앞으로 50 이동합니다.
    t.right(30)#오른쪽으로 30도 회전합니다.
    t.forward(30)#다시 앞으로 30이동합니다.
    t.right(150)#다시 오른쪽으로 150 이동합니다.
    t.fd(30)#다시 앞으로 30 이동합니다.
    t.right(30)#오른쪽으로 30 회전합니다.
    t.fd(150)#앞으로 150 이동합니다.
